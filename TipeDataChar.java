/*
1. Data Character di java dipresentasaikan oleh tipe data char
2. Untuk membuat data char di java, kita bisa menggunakan tanda ' (petik satu) di awal dan di akhir karakter
*/

public class TipeDataChar {
  public static void main(String[] args) {
    char j = 'J' ;
    char k = 'K' ;
    char l = 'L' ;
    
    System.out.println(j) ; 
    System.out.println(k) ;
    System.out.println(l) ;
    
    /*  ---Terminal---
    J
    K
    L
    */
    
    System.out.print(j) ;
    System.out.print(k) ;
    System.out.print(l) ;
    
    /* ---Terminal---
    JKL
    
    nb: jd println itu adalah setelah baris code (enter), jadi kalau print biasa akan jadi satu baris code
    */
    
      }
}
